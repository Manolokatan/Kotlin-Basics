package thebasics.UsingWhile

fun main(args : Array<String>){

    //type = Int
    val aNumber :Int  = 100// this only excepts integers
    print(aNumber) // print aNumber
    print("\n") // go down a row

    //type = Double
    val aDoubleNumber : Double = -1.5// this excepts integers and fractions like 2.4
    print(aDoubleNumber)
    print("\n")

    //type = Char
    val aChar = 'f'// this is a single character that can be anything
    print(aChar)
    print("\n")

    //type = String
    val aString = "Hello, World"// this is like a list of Chars, like "hello there"
    print(aString)
    print("\n")

    //type = Boolean
    val aBool = true// this can hold a value of true or false
    print(aBool)
    print("\n")



}
